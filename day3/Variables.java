import java.lang.*;

class Variables {

	public static void main(String[] args) {

		byte b = 127;
		short s = 30000;
		int i = 12000000;

		float f = 12.45f;
		double d = 36.000000009D;
		
		char c = 'j';

		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);

	}

}