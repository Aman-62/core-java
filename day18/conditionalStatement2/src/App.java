public class App {
    public static void main(String[] args) throws Exception {
        int a = 5, b = 10, c = 15;

        System.out.println(a > c);
        System.out.println(b >= 10);
        System.out.println(a == 15);
        System.out.println(a != 10);

        System.out.println(a < b && a > c);
        System.out.println(a > b || a > c);
    }
}

